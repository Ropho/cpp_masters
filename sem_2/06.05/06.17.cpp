////////////////////////////////////////////////////////////////////////////////

#include <exception>
#include <iostream>
#include <string>

////////////////////////////////////////////////////////////////////////////////

#include <boost/dll.hpp>

////////////////////////////////////////////////////////////////////////////////

int main()
{
	std::string library;

//  ----------------------------------------------------------------------------

	while (std::cin >> library)
	{
		try
		{
			boost::dll::import_symbol < void() > (library, "test")();
		}
		catch (const std::exception & exception)
		{
			std::cerr << exception.what() << std::endl;
		}
	}
}

